#include <iostream>

int main() {
    // O loop começará com i=1, continuará enquanto i for menor ou igual a 5 e incrementará i a cada iteração
    for (int i = 1; i <= 5; i++) {
        // Imprime o valor atual de i
        std::cout << "Número: " << i << std::endl;
    }

    // Mensagem após o término do loop
    std::cout << "Fim do programa." << std::endl;

    return 0;
}
